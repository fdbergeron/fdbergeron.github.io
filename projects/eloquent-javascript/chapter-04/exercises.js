// range ///////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////

function range(start, end, step) {
  let resultArray = [];
  if (!step) {
    step = 1;
    
  }
  if (start < end && step > 0) {
    while (start < end + 1) {
      resultArray.push(start);
      start += step;
      
    }
    
  } else if (start > end && step < 0) {
    while (start > end - 1) {
      resultArray.push(start);
      start += step;
      
    }
    
  } else {
    return resultArray;
    
  }
  return resultArray;
}

