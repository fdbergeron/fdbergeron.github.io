// reverseArray ////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////

function reverseArray(array) {
  let newReversedArray = [];
  for (let i = 0; i < array.length; i++) {
    newReversedArray.unshift(array[i]);
  }
  return newReversedArray;

}

